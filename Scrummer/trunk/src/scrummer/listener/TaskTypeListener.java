package scrummer.listener;

import scrummer.enumerator.TaskTypeOperation;
import scrummer.enumerator.DataOperation;

public interface TaskTypeListener {
	
	/**
	 * Operation was successful
	 * 
	 * @param type operation type
	 * @param identifier operation identifier
	 * @param message operation message
	 */
	void operationSucceeded(DataOperation type, TaskTypeOperation identifier, String message);
	
	/**
	 * Operation failed
	 * 
	 * @param type operation type
	 * @param identifier operation identifier
	 * @param message failure message
	 */
	void operationFailed(DataOperation type, TaskTypeOperation identifier, String message);
}
